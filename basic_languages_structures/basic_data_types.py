# This file covers Numbers and Strings only.

# Number Examplee
print("Number examples")
floatingPointNumber = 66.6
print("Floating Point example:", floatingPointNumber, type(floatingPointNumber))
decimalNumber = 6666
print("Decimal example:", decimalNumber, "type :", type(decimalNumber))
complexNumber = 15 + 7j
print("Complex example:", complexNumber, "type :", type(complexNumber))
print()

